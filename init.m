CM=[500:100:2400]';%一个bit需要的cpu周期数(500,2400)cycles/bit
DM=[100:20:480]'.*1e3;%每个CUE用户的数据量(100,480)KB
DK=DM;%每个DUE用户的数据量(100,480)KB
HM=[-121:1:-102]';%信道增益,(-120,-100)dbm
FCM=2e10/M;%每个CUE用户分配的计算速率（20G/M）
PML=1e-8;%CUE本地耗能功率
PM=2;%CUE最大发射功率w
PKD_max_dbm=23;%DUE最大发射功率
